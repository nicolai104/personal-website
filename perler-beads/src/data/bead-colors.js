export function getBrandList() {
  return beadColors.brands.map(brand => ({
    id: brand.id,
    name: brand.name,
    fullName: brand.fullName,
    colorCount: brand.colors.length
  }))
}

export function getBrandColors(brandId) {
  const brand = beadColors.brands.find(b => b.id === brandId)
  return brand ? brand.colors : []
}

export function getColorByCode(brandId, code) {
  const brand = beadColors.brands.find(b => b.id === brandId)
  if (!brand) return null
  return brand.colors.find(c => c.code === code)
}
